// OLED 模組 RESET 腳位
#define OLED_RESET 4

// 建立 128x64 SSD1306 顯示物件
Adafruit_SSD1306 display(128, 64, &Wire, OLED_RESET);

void setup() {
  // 初始化 SSD1306
  display.begin(SSD1306_SWITCHCAPVCC, 0x3c); // Address 0x3C for 128x64

  // 清除畫面
  display.clearDisplay();

  // 設定預設文字大小為 1
  display.setTextSize(1);

  // 設定為白字
  display.setTextColor(SSD1306_WHITE);

  // 將目前緩衝內容送到螢幕（此時是全黑）
  display.display();
}

void loop() {
  // 顯示睜眼和學號
  display.clearDisplay();// 清空畫面

  //從左上角開始生成圖片
  display.drawBitmap(0, 0, image1, 128, 32, SSD1306_WHITE);

  display.setTextSize(1);// 文字大小 1
  display.setCursor(40, 48);// 將游標移到 (40,48)，約在下半部置中
  display.print("4B3G0118");//印出學號
  display.display();// 將影像與文字更新到 OLED
  delay(500);

  // 顯示閉眼和學號
  display.clearDisplay();

  display.drawBitmap(0, 0, image2, 128, 32, SSD1306_WHITE);

  display.setTextSize(1);
  display.setCursor(40, 48);
  display.print("4B3G0118");
  display.display();
  delay(500);
}
